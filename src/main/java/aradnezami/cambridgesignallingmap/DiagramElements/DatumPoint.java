package aradnezami.cambridgesignallingmap.DiagramElements;

/**
 * A temporary skeleton for the datum point class, simply storing a position, which relevant elements
 * may recall.
 */
public class DatumPoint {
    public int x;
    public int y;

    public DatumPoint(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
